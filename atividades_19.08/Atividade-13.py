for numero in range(1, 101):
    if numero % 2 != 0:
         print(numero,end=" ") 
#Este código faz um laço de repetição com o for, a variável número guardará os números de 1 à 101
#O if numero % 2 != 0, verifica se o número é impar dividindo os números por 2 e verificando se são diferentes de 0, portanto, impares
#Se forem de fato impares o código os exibirá, se forem pares não irá exibir, então aparecerá apenas os números 1,3,5,7... até o 99